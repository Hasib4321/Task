package com.src;

public class DemoEmployee {
private int EmpNo;
private String Name;
private int Salary;
public int getEmpNo() {
	return EmpNo;
}
public void setEmpNo(int empNo) {
	EmpNo = empNo;
}
public String getName() {
	return Name;
}
public void setName(String name) {
	Name = name;
}
public int getSalary() {
	return Salary;
}
public void setSalary(int salary) {
	Salary = salary;
}}